function vos1(id_) {
           var id = String(id_);
           var el = document.getElementById("vos"+id+"_1");
           if (el.name == 0){
              el.style.backgroundColor = 'IndianRed';
              el.name = 1;
           }
           else {
              el.style.backgroundColor = '#fff0f5';
              el.name = 0;
           }
}

